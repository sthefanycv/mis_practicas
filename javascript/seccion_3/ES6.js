/*
    ¿Que es el ES6?
    ES6 es la sexta versión de ECMAScript, que es el estándar en el que se basa JavaScript,
    esta version fue lanzada en el 2015 y trajo consigo muchas mejoras y nuevas funcionalidades
    haciendo que JavaScript sea un lenguaje de programación más poderoso y eficiente, y a su vez ganando
    mayor popularidad en la comunidad de desarrolladores.

    Entre las características más importantes de ES6 tenemos:

        - Destructuring: Es una forma más sencilla de extraer valores de un objeto o un array en JavaScript. 
            Ej: const { nombre, edad } = persona;
        
        - spread operator: Es una forma más sencilla de combinar objetos o arrays en JavaScript. 
            Ej: const nuevoArray = [...array1, ...array2];
        
        - Arrow Functions o Funciones Flecha: Son una forma más corta de escribir funciones en JavaScript. 
            Ej: (param1, param2) => { return param1 + param2; }
        
        - Template Strings o Strings Literals: Son una forma más sencilla de concatenar cadenas de texto en JavaScript. 
            Ej: `Hola ${nombre}!`
        
        - Let y Const: Son nuevas formas de declarar variables en JavaScript. Let es similar a var pero con un alcance de bloque y const es una constante.
            Ej: let nombre = 'Juan'; const edad = 30;
        
        - Clases: Son una forma más sencilla de crear objetos en JavaScript. 
            Ej: class Persona { constructor(nombre) { this.nombre = nombre; } }
        
        - Promesas: Son una forma más sencilla de trabajar con código asíncrono en JavaScript. 
            Ej: new Promise((resolve, reject) => { resolve(); });
        
        - Módulos: Son una forma más sencilla de organizar y reutilizar código en JavaScript. 
            Ej: import { nombre } from './modulo.js';
        
        - metodo map: Es una forma más sencilla de recorrer un array en JavaScript. 
            Ej: const nuevoArray = array.map((elemento) => { return elemento * 2; });
            
*/ 